    CAPTION = "CAPTION"
    TABLE = "TABLE"
    MAP_LABEL = "MAP_LABEL"
    DIAGRAM_LABEL = "DIAGRAM_LABEL"
    PHOTO_CREDIT = "PHOTO_CREDIT"
    OCR_ARTIFACT = "OCR_ARTIFACT"
    OTHER = "OTHER"
